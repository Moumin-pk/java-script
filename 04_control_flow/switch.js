// switch => use for multiple condition

let key = 2;
switch (key) {
    case 1:
        console.log(`january`);
        break;
    case 2:
        console.log(`Feburary`);
        break;
    case 3:
        console.log(`March`);
        break;

    default:
        console.log(`condition is not enougth`);
        break;
}